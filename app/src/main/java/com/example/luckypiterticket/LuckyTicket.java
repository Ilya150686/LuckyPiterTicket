package com.example.luckypiterticket;

public class LuckyTicket {
    private int number;

    public LuckyTicket() {
    }

    public LuckyTicket(int number) {
        this.number = number;
    }

    /**
     * ?????, ??????????? ????? ?? ????????????
     *
     * ????? ???? ????? ? ??????? abcdef
     * ????? ???????? ???? s1 = b + d + f
     *  ????? ?????? ???? s2 = a + c + e
     *
     *  ????? ???????? ??????????, ????  s1 = s2
     * @return ???????? ?? ????? ??????????
     */
    public boolean isLucky() {
        int sum = 0;
        for (int i = 0; i < 6; i++) {
            sum += Math.pow(-1, i) * (number / ((int) Math.pow(10, i)) % 10);
        }
        /*
        ???????, ??? ????? ??????????, ????  s2 - s1 = 0
        ? ??????? ??????? ????? -1 ?? ???????? ???????? ????? ? ?????????? ????????
        ???????? ???? ???????:
        sum = f - e + d - c + b - a = (b + d + f) - (a + c + e) = s2 - s1
        ????? ????? ??????????, ???? sum = 0
         */
        return sum  == 0;
    }

    /**
     * ????? ?????? ?????????? ??????????? ??????
     * @return ????? ?????????? ??????????? ??????
     */
    public int findNextLucky() {
        int res = number;

        for (; res < 1_000_000; res++) {
            int sum = 0;
            for (int i = 0; i < 6; i++) {
                sum += Math.pow(-1, i) * (res / ((int) Math.pow(10, i)) % 10);
            }

            if (sum == 0) {
                break;
            }
        }
        return res;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}